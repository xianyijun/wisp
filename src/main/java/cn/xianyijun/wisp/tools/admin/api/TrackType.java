package cn.xianyijun.wisp.tools.admin.api;

/**
 * The enum Track type.
 */
public enum  TrackType {
    /**
     * Consumed track type.
     */
    CONSUMED,
    /**
     * Consumed but filtered track type.
     */
    CONSUMED_BUT_FILTERED,
    /**
     * Pull track type.
     */
    PULL,
    /**
     * Not consume yet track type.
     */
    NOT_CONSUME_YET,
    /**
     * Not online track type.
     */
    NOT_ONLINE,
    /**
     * Unknown track type.
     */
    UNKNOWN
}
